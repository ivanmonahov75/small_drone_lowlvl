/**
 * @brief  Function implementing the defaultTask thread.
 * @param  argument: Not used
 * @retval None
 */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
  /* USER CODE BEGIN 5 */
	// does all except for serial data processing
	/* Infinite loop */
	MPU6050_init(&hi2c1, SAMPLING_RATE_1KHZ, GYRO_500DS, ACCEL_8G); // init mpu6050
	MPU6050_calibrate(1000);
	BMP180_initCalibrate(&hi2c1);

	float error_x = 0, error_y = 0; // init values for kalman filter
	float error_prev_x = 0, error_prev_y = 0;
	float iterm_prev_x = 0, iterm_prev_y = 0;
	float out_x = 0, out_y = 0;

	// set start time
	uint32_t time_from_start = xTaskGetTickCount();
	HAL_GPIO_WritePin(GPIOC, GPIO_PIN_13, 0);

	for (;;) {
		MPU6050_getAnglesKalman(angles_kal, time_from_start);

		// calculate PID
		error_x = 2 * (angles_des[0] - angles_kal[0]);
		error_y = 2 * (angles_des[1] - angles_kal[1]);

		calculatePID_xy(error_x, &error_prev_x, &iterm_prev_x, &out_x);
		calculatePID_xy(error_y, &error_prev_y, &iterm_prev_y, &out_y);

		// TODO do someth with z angle
		//		calculatePID_z(error_z, &error_prev_z, &iterm_prev_z, &out_z);

		motors[0] = roundf(1000 + out_x + out_y);
		motors[1] = roundf(1000 - out_x + out_y);
		motors[2] = roundf(1000 - out_x - out_y);
		motors[3] = roundf(1000 + out_x - out_y);
		norm_motors(motors);
		motors[0] += 50; // not really necessary, just for fun
		motors[1] += 50;
		motors[2] += 50;
		motors[3] += 50;

		//		write_motors(motors); // send values to motors

		// use ADC to get battery state
		HAL_ADC_Start(&hadc1);
		HAL_ADC_PollForConversion(&hadc1, 20);
		battery_out[0] = HAL_ADC_GetValue(&hadc1);

		// communication with computer is done in usbd_cdc_if.c
		while (xTaskGetTickCount() - time_from_start < 4) {
		}
		time_from_start = xTaskGetTickCount();

	}
  /* USER CODE END 5 */
}

/* USER CODE BEGIN Header_StartTaskSerial */
/**
 * @brief Function implementing the taskSerial thread.
 * @param argument: Not used
 * @retval None
 */
/* USER CODE END Header_StartTaskSerial */
void StartTaskSerial(void *argument)
{
  /* USER CODE BEGIN StartTaskSerial */
	// for processing only serial data
	/* Infinite loop */
	for (;;) {
		if (updated == 1) {
			switch (exchange_data[0]) {
			case GET_ANGLES:
				memcpy(angles_des, exchange_data + 1, sizeof(angles_des));
				break;

			case SEND_ANGLES:
				CDC_Transmit_FS(angles_kal, sizeof(angles_kal));
				break;

			case GET_SPEEDS:
				// do not do anything
				break;

			case SEND_SPEEDS:
				// do not do anything

				break;

			case SEND_BATTERIES:
				CDC_Transmit_FS(battery_out, 2);
				break;

			case GET_SERVOS:
				memcpy(servos_in, exchange_data + 1, sizeof(servos_in));
				break;

			}
		}

		osDelay(1);
	}
  /* USER CODE END StartTaskSerial */
}

